package ch03_operator;

public class Operator01 {
	public static void main(String[] args) {
		int num1, num2, result;
		
		num1 = 10;
		num2 = 5;
		
		result = num1 + num2;  // 덧셈
		result = num1 - num2;  // 뺄셈
		result = num1 * num2;  // 곱셈
		result = num1 / num2;  // 나눗셈(몫)
		result = num1 % num2;  // 나눗셈(나머지)		
	}
}
