/*
 * 상수
 *  - 변수 앞에 'final' 키워드를 추가하면 상수로 변환
 *  - 상수의 변수명은 대문자와 _를 사용
 *    ex) MAX_VALUE
 *  - 상수는 1번 초기화 후 변경 불가
 *  - 프로젝트에서 팀원들이 공유하는 값 또는 설정값에 활용
 */


package ch02_variable;

public class Constant {
	public static void main(String[] args) {
		final int NUM = 4; // 상수, 변하지 않는 값, 대문자
	}
}
